void GLCD_SelectPage0(void)
{
	CONTROL &= ~CS2;
	CONTROL |= CS1;
}

void GLCD_SelectPage1(void)
{
	CONTROL &= ~CS1;
	CONTROL |= CS2;
}

void GLCD_CmdWrite(uint8_t cmd)
{
	DATA = cmd;
	CONTROL &= ~RS;
	CONTROL |= EN;
	__delay_us(500);
	CONTROL &= ~EN;
	__delay_us(500);
}

void GLCD_DataWrite(uint8_t dat)
{
	DATA = dat;
	CONTROL |= RS | EN;
	__delay_us(500);
	CONTROL &= ~EN;
	__delay_us(500);
}

void GLCD_Init(void)
{
	__delay_ms(10);
	GLCD_SelectPage0();
	GLCD_CmdWrite(GLCD_DISPLAY_ON);
	GLCD_SelectPage1();
	GLCD_CmdWrite(GLCD_DISPLAY_ON);
	__delay_ms(1);

	GLCD_SelectPage0();
	GLCD_CmdWrite(GLCD_SETZ_ADDRESS);
	GLCD_SelectPage1();
	GLCD_CmdWrite(GLCD_SETZ_ADDRESS);
	__delay_us(200);
}

void GLCD_DisplayChar(char c)
{
	int i, offset;

	if(c < 0x20U)
		offset = 0;
	else
		offset = c - 0x20U;

	uint8_t *ptr_array = &A_GlcdFontTable_U8[offset][0];

	GLCD_DataWrite(0);

	for(i=0;i<5;i++) // 5x7 font, 5 chars + 1 blankspace
		GLCD_DataWrite(ptr_array[i]);

	GLCD_DataWrite(0);
}